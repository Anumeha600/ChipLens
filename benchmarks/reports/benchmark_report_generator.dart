import 'dart:io';

import '../models/benchmark_models.dart';
import 'report.dart';

// ─── BenchmarkReportGenerator ─────────────────────────────────────────────────

/// Converts a [BenchmarkSuiteResult] into a Markdown report.
///
/// All values in the generated report are derived from the actual
/// [BenchmarkSuiteResult] — nothing is hardcoded.
///
/// Usage:
/// ```dart
/// const generator = BenchmarkReportGenerator();
/// final markdown = generator.generate(suiteResult);
/// generator.writeToFile(suiteResult);   // writes to ReportConfig.outputPath
/// ```
class BenchmarkReportGenerator {
  const BenchmarkReportGenerator();

  // ── Public API ─────────────────────────────────────────────────────────────

  /// Returns the full Markdown report as a [String].
  String generate(
    BenchmarkSuiteResult suite, {
    ReportConfig config = const ReportConfig(),
  }) {
    final buf = StringBuffer();

    _writeHeader(buf, config);
    _writeDesignTable(buf, suite, config);
    _writeSummary(buf, suite);

    return buf.toString();
  }

  /// Generates the report and writes it to [config.outputPath].
  ///
  /// Creates parent directories if they do not exist.
  void writeToFile(
    BenchmarkSuiteResult suite, {
    ReportConfig config = const ReportConfig(),
  }) {
    final content = generate(suite, config: config);
    final file = File(config.outputPath);
    file.parent.createSync(recursive: true);
    file.writeAsStringSync(content);
  }

  // ── Section writers ────────────────────────────────────────────────────────

  void _writeHeader(StringBuffer buf, ReportConfig config) {
    buf.writeln('# ChipLens Benchmark Results');
    buf.writeln();
    if (config.includeDate) {
      final date = DateTime.now().toIso8601String().substring(0, 10);
      buf.writeln('**Date:** $date');
      buf.writeln();
    }
    buf.writeln(
      'Generated by `BenchmarkReportGenerator` from actual pipeline execution. '
      'All values are measured at run time.',
    );
    buf.writeln();
  }

  void _writeDesignTable(
    StringBuffer buf,
    BenchmarkSuiteResult suite,
    ReportConfig config,
  ) {
    buf.writeln('## Design Analysis');
    buf.writeln();

    if (config.includeNotes) {
      buf.writeln('| Design | Diagnostics | Repairs | Runtime (ms) | Success | Notes |');
      buf.writeln('|--------|-------------|---------|--------------|---------|-------|');
      for (final r in suite.results) {
        buf.writeln(
          '| ${r.designName} | ${r.diagnosticCount} | ${r.repairCount} '
          '| ${r.runtimeMs} | ${_yesNo(r.success)} | ${r.notes ?? ''} |',
        );
      }
    } else {
      buf.writeln('| Design | Diagnostics | Repairs | Runtime (ms) | Success |');
      buf.writeln('|--------|-------------|---------|--------------|---------|');
      for (final r in suite.results) {
        buf.writeln(
          '| ${r.designName} | ${r.diagnosticCount} | ${r.repairCount} '
          '| ${r.runtimeMs} | ${_yesNo(r.success)} |',
        );
      }
    }

    buf.writeln();
  }

  void _writeSummary(StringBuffer buf, BenchmarkSuiteResult suite) {
    buf.writeln('## Summary');
    buf.writeln();

    final successResults =
        suite.results.where((r) => r.success).toList();

    final avgRuntime = successResults.isEmpty
        ? 0
        : (successResults.map((r) => r.runtimeMs).reduce((a, b) => a + b) /
                successResults.length)
            .round();

    final totalDiagnostics =
        suite.results.fold<int>(0, (sum, r) => sum + r.diagnosticCount);

    final totalRepairs =
        suite.results.fold<int>(0, (sum, r) => sum + r.repairCount);

    buf.writeln('| Metric | Value |');
    buf.writeln('|--------|-------|');
    buf.writeln('| Total Designs | ${suite.totalDesigns} |');
    buf.writeln('| Successful Runs | ${suite.successfulDesigns} |');
    buf.writeln('| Failed Runs | ${suite.failedDesigns} |');
    buf.writeln('| Average Runtime (ms) | $avgRuntime |');
    buf.writeln('| Total Diagnostics | $totalDiagnostics |');
    buf.writeln('| Total Repairs | $totalRepairs |');
  }

  // ── Helpers ───────────────────────────────────────────────────────────────

  static String _yesNo(bool value) => value ? 'Yes' : 'No';
}

// ── Standalone summary helpers ─────────────────────────────────────────────────

/// Returns the average runtime in milliseconds across [results].
/// Returns 0 when [results] is empty.
int averageRuntimeMs(List<BenchmarkResult> results) {
  if (results.isEmpty) return 0;
  final total = results.fold<int>(0, (sum, r) => sum + r.runtimeMs);
  return (total / results.length).round();
}

/// Returns the sum of all [BenchmarkResult.diagnosticCount] values.
int totalDiagnostics(List<BenchmarkResult> results) =>
    results.fold<int>(0, (sum, r) => sum + r.diagnosticCount);

/// Returns the sum of all [BenchmarkResult.repairCount] values.
int totalRepairs(List<BenchmarkResult> results) =>
    results.fold<int>(0, (sum, r) => sum + r.repairCount);
